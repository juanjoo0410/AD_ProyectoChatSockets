    private void initServer() {
        try (ServerSocket server = new ServerSocket(PORT)) {
            txtLog.append("Servidor Iniciado...\n");
            txtLog.append("Esperando que se conecten clientes...\n");
            lbStatus.setText("Server ONLINE Port: " + PORT);
            lbStatus.setForeground(Color.BLUE);
            btnStart.setText("Exit");

            while (running) {
                Socket client = server.accept();
                txtLog.append("Cliente conectado desde " + client.getInetAddress() + "\n");

                // Crea una nueva instancia de ChatService
                ChatService servicio = new ChatService(client, chatRoom);
                servicio.setAddress(client.getInetAddress().toString());

                // Crea un nuevo hilo para el servicio y comienza la ejecuci√≥n
                Thread hilo = new Thread(servicio);
                hilo.start();
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    
    public static void updateUserList(List<String> users) {
        SwingUtilities.invokeLater(() -> {
            txtUsers.setText("");
            for (String user : users) {
                txtUsers.append(user + "\n");
            }
        });
    }

